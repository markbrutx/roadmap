// Какие типы данных есть в JavaScript?


console.log(typeof 42);         // Logs: number
console.log(typeof 'Hello');    // Logs: string
console.log(typeof true);       // Logs: boolean
console.log(typeof null);       // Logs: object
console.log(typeof undefined);  // Logs: undefined
console.log(typeof {});         // Logs: object
console.log(typeof Symbol());   // Logs: symbol














// Устный ответ: В JavaScript есть семь основных типов данных: 
// number, string, boolean, null, undefined, object и symbol.

// Обширный ответ с пояснениями:

// В приведенном примере мы используем оператор typeof, 
// чтобы определить тип каждого значения. 
// Заметьте, что typeof null возвращает 'object', 
// что является исторической особенностью JavaScript.